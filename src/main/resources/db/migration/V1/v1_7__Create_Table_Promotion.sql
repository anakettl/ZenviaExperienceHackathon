CREATE SEQUENCE SEQUENCE_PROMOTION START WITH 1 INCREMENT BY 1;

CREATE TABLE PROMOTION (
    UID_PROMOTION BIGINT NOT NULL,
    NAME VARCHAR(200) NOT NULL,
    BEGIN_DATE TIMESTAMP NOT NULL,
    END_DATE TIMESTAMP NOT NULL,
    DESCRIPTION VARCHAR(400) NOT NULL,
    DISCOUNT INTEGER NOT NULL,
    FK_COMPANY_UID BIGINT NOT NULL,
    CREATED_AT TIMESTAMP NOT NULL
);

ALTER TABLE PROMOTION ADD CONSTRAINT PK_PROMOTION PRIMARY KEY(UID_PROMOTION);
ALTER TABLE PROMOTION ADD CONSTRAINT FK_PROMOTION_UID_COMPANY FOREIGN KEY (FK_COMPANY_UID) REFERENCES COMPANY (UID_COMPANY);

